// Déclarer un tableau de prénoms.
// Avec une boucle foreach, affiche chaque prénom sur une nouvelle ligne.

string[] prenoms = { "Alise", "Bob", "Charles", "Diana" };

foreach (string prenom in prenoms)
{
    Console.WriteLine(prenom);
}

